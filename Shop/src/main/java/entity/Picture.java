package entity;

public class Picture {
	private Integer id;
	private String index;
	private String path;


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCover() {
		return index;
	}

	public void setCover(String index) {
		this.index = index;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

}
